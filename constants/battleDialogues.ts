// ==================== 輔助函數 ====================

/**
 * 從對話陣列中隨機取得一句台詞
 * @param dialogues 台詞陣列
 * @param excludeLast 排除上一次使用的台詞（避免連續重複）
 */
export function getRandomDialogue(
    dialogues: string[],
    excludeLast?: string
): string {
    if (dialogues.length === 0) return '';
    if (dialogues.length === 1) return dialogues[0];

    let available = dialogues;
    if (excludeLast) {
        available = dialogues.filter(d => d !== excludeLast);
    }

    const index = Math.floor(Math.random() * available.length);
    return available[index];
}
